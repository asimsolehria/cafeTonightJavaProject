    public void Backupdbtosql() {
        String path = null;
        Process pr;
        JFileChooser jfc= new JFileChooser();
        
        int rsp = jfc.showDialog(this,"Save Backup");
        if(rsp==JFileChooser.APPROVE_OPTION){
        // getting date  
        String date=new SimpleDateFormat("yyy-MM-dd").format(new Date());
    try {
        File f= jfc.getSelectedFile();
        path=f.getAbsolutePath();
        path=path.replace('\\', '/');
        
        // attaching date with file path
        path = path + "_" + date + ".sql";

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
 }
        int x=path.length();
 if(x>0)
    {
    try {
        Runtime run= Runtime.getRuntime();
        pr=run.exec("C:\\xampp\\mysql\\bin\\mysqldump.exe -uroot --add-drop-database -B redngreenchillijava -r"+ path);

        int processComplete= pr.waitFor();
        if(processComplete==0)
        {
        JOptionPane.showMessageDialog(null, "Backup Success");
        }
        else
        {
        JOptionPane.showMessageDialog(null, "Backup Failed");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e+" Error in backup");
    }
    }else
    {
            JOptionPane.showMessageDialog(null, "Select a Path to store Database.");
    }
    
        }
        
       }

     
